def encrypt(text, shift):
    cipher_text = ''
    for letter in text:
        pos = alphabet.index(letter)
        new_pos = pos + shift
        new_letter = alphabet[new_pos]
        
        cipher_text += new_letter
    print(cipher_text)
    return cipher_text

def decrypt(cipher_text, shift):
    text = ''
    for letter in cipher_text:
        pos = alphabet.index(letter)
        new_pos = pos - shift
        new_letter = alphabet[new_pos]
        
        text += new_letter
    print(text)
    return text

